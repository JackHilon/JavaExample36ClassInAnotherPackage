
package MyMath;


public class ConvertAngle {
    
    public static double Convert2Radian(double deg)
    {
        double res=Math.toRadians(deg);
        return res;
    }
    
    public static double Convert2Degree(double radian)
    {
        double res=Math.toDegrees(radian);
        return res;
    }
}
